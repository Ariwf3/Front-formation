                                                             //~~ L'Addition ~~ //


    //-- Declaration de plusieurs variables à la suite
    var nb1, nb2, resultat;
    nb1 = 10;
    nb2 = 5;

    //-- Addition de nb1 + nb2 avec l'opérateur "+"
    resultat = nb1 + nb2;

    //-- Affichage du resultat dans la console
    console.log("L'addition de nb1 + nb2 est égale à : " + resultat );


                                                             //~~ La soustraction ~~//
    
    //-- Soustraction de nb1 et nb2 avec l'opérateur "+"
    resultat = nb1 - nb2;

    //-- Affichage du resultat dans la console
    console.log("La soustraction de nb1 - nb2 est égale à : " + resultat );



                                                            //~~ La multiplication ~~//
    
    //-- Multiplication de nb1 et nb2 avec l'opérateur "*"
    resultat = nb1 * nb2;

    //-- Affichage du resultat dans la console
    console.log("La multiplication de nb1 - nb2 est égale à : " + resultat );



                                                                //~~ La division ~~//
    
    //-- Division de nb1 et nb2 avec l'opérateur "*"
    resultat = nb1 / nb2;

    //-- Affichage du resultat dans la console
    console.log("La division de nb1 - nb2 est égale à : " + resultat );



                                                                //~~ Le Modulo~~//
    
  //~~ LE MODULO de nb1 ET nb2 avec l'opérateur "*" 
    nb1 = 11;
